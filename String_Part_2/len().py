# len() -- string method used to find / calculate whole string length which contains number, whitespaces, special character, all character.

print(len("tree"))
# OutPut:- 4

print("This" + " " + "is a " + "string.")
# OutPut:- This is a string.

print(len("This" + " " + "is a " + "string."))
# OutPut:- 17

